	<header><strong>User Story</strong></header>
</div>
<div>
	<p>
		As a user I would like to create a profile for a lost/missing dog which will contains the dogs information. As a user I need the ability to
		export and/or share the profile in a efficient timely matter.
	</p>
	<div>
		<header><strong>Requirements</strong></header>
		<ol>
			<li>Button to create a dog profile</li>
			<li>Dog profile forum</li>
			<li>Button to save profile</li>
			<li>Button to share profile via link</li>
			<li>Button to share profile via pdf</li>
		</ol>
		<header><strong>Acceptance criteria</strong></header>
		<ol>
			<li>User is able to create a dog profile in a timely manner ~less than 5 minutes</li>
			<li>User is able to create dog profile on mobile device</li>
			<li>User is able to share dog profile via link</li>
			<li>User is able to export dog profile via pdf</li>
		</ol>
	</div>
	<div>
		<header><strong>Use Case</strong></header>
		<ol>
			<li>
				User logs into site on mobile device
			</li>
			<li>
				User selects on Create Dog Profile button
			</li>
			<li>
				New window appears with empty fields to enter dog information
			</li>
			<li>
				User enters dog information
			</li>
			<li>
				User saves profile
			</li>
			<li>
				User has the ability to share dog profile by selecting Share or Export buttons
			</li>
		</ol>
	</div>
</div>
